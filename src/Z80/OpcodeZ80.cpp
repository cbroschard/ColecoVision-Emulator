static Z80OperandMode getIndexedCBRegister(uint8_t opcode)
{
    switch (opcode & 0x07)
    {
        case 0x00: return Z80OperandMode::B;
        case 0x01: return Z80OperandMode::C;
        case 0x02: return Z80OperandMode::D;
        case 0x03: return Z80OperandMode::E;
        case 0x04: return Z80OperandMode::H;
        case 0x05: return Z80OperandMode::L;
        case 0x06: return Z80OperandMode::None; // memory-only form
        case 0x07: return Z80OperandMode::A;
    }

    return Z80OperandMode::None;
}

static Z80OperandMode getBitOperand(uint8_t opcode)
{
    switch ((opcode >> 3) & 0x07)
    {
        case 0x00: return Z80OperandMode::Bit0;
        case 0x01: return Z80OperandMode::Bit1;
        case 0x02: return Z80OperandMode::Bit2;
        case 0x03: return Z80OperandMode::Bit3;
        case 0x04: return Z80OperandMode::Bit4;
        case 0x05: return Z80OperandMode::Bit5;
        case 0x06: return Z80OperandMode::Bit6;
        case 0x07: return Z80OperandMode::Bit7;
    }

    return Z80OperandMode::Bit0;
}

static Z80InstructionInfo makeDDCBEntry(uint8_t opcode)
{
    Z80InstructionInfo info =
    {
        "???",
        Z80OperandMode::None,
        Z80OperandMode::None,
        4,
        opcode,
        Z80OpcodePage::DDCB
    };

    const Z80OperandMode indexedOperand = Z80OperandMode::AddrIXd;
    const Z80OperandMode registerOperand = getIndexedCBRegister(opcode);

    if (opcode <= 0x3F)
    {
        static const char* rotateShiftNames[8] =
        {
            "RLC", "RRC", "RL", "RR", "SLA", "SRA", "SLL", "SRL"
        };

        info.mnemonic = rotateShiftNames[(opcode >> 3) & 0x07];
        info.op1 = indexedOperand;
        info.op2 = registerOperand;

        return info;
    }

    if (opcode >= 0x40 && opcode <= 0x7F)
    {
        info.mnemonic = "BIT";
        info.op1 = getBitOperand(opcode);
        info.op2 = indexedOperand;

        return info;
    }

    if (opcode >= 0x80 && opcode <= 0xBF)
    {
        info.mnemonic = "RES";
        info.op1 = getBitOperand(opcode);
        info.op2 = indexedOperand;

        return info;
    }

    if (opcode >= 0xC0)
    {
        info.mnemonic = "SET";
        info.op1 = getBitOperand(opcode);
        info.op2 = indexedOperand;

        return info;
    }

    return info;
}

static std::array<Z80InstructionInfo, 256> makeDDCBTable()
{
    std::array<Z80InstructionInfo, 256> table{};

    for (int i = 0; i < 256; ++i)
        table[i] = makeDDCBEntry(static_cast<uint8_t>(i));

    return table;
}

static Z80InstructionInfo makeFDCBEntry(uint8_t opcode)
{
    Z80InstructionInfo info = makeDDCBEntry(opcode);

    info.page = Z80OpcodePage::FDCB;

    if (info.op1 == Z80OperandMode::AddrIXd)
        info.op1 = Z80OperandMode::AddrIYd;

    if (info.op2 == Z80OperandMode::AddrIXd)
        info.op2 = Z80OperandMode::AddrIYd;

    return info;
}

static std::array<Z80InstructionInfo, 256> makeFDCBTable()
{
    std::array<Z80InstructionInfo, 256> table{};

    for (int i = 0; i < 256; ++i)
        table[i] = makeFDCBEntry(static_cast<uint8_t>(i));

    return table;
}

const std::array<Z80InstructionInfo, 256> Z80_DDCB_OPCODES = makeDDCBTable();
const std::array<Z80InstructionInfo, 256> Z80_FDCB_OPCODES = makeFDCBTable();

static bool isAssemblerVisibleInstruction(const Z80InstructionInfo& info)
{
    if (info.mnemonic == nullptr)
        return false;

    std::string mnemonic = info.mnemonic;

    if (mnemonic.empty())
        return false;

    if (mnemonic == "???")
        return false;

    if (mnemonic == "PREFIX")
        return false;

    return true;
}

static void addInstructionToMnemonicMap(
    std::unordered_map<Z80MnemonicKey, Z80InstructionInfo, Z80MnemonicKeyHash>& map,
    const Z80InstructionInfo& info)
{
    if (!isAssemblerVisibleInstruction(info))
        return;

    Z80MnemonicKey key
    {
        info.mnemonic,
        info.op1,
        info.op2,
        info.page
    };

    map[key] = info;
}

static void addRawOpcodeTableToMnemonicMap(
    std::unordered_map<Z80MnemonicKey, Z80InstructionInfo, Z80MnemonicKeyHash>& map,
    const Z80InstructionInfo table[256])
{
    for (int i = 0; i < 256; ++i)
        addInstructionToMnemonicMap(map, table[i]);
}

static void addArrayOpcodeTableToMnemonicMap(
    std::unordered_map<Z80MnemonicKey, Z80InstructionInfo, Z80MnemonicKeyHash>& map,
    const std::array<Z80InstructionInfo, 256>& table)
{
    for (const Z80InstructionInfo& info : table)
        addInstructionToMnemonicMap(map, info);
}

static std::unordered_map<Z80MnemonicKey, Z80InstructionInfo, Z80MnemonicKeyHash>
buildMnemonicToOpcodeMap()
{
    std::unordered_map<Z80MnemonicKey, Z80InstructionInfo, Z80MnemonicKeyHash> map;

    addRawOpcodeTableToMnemonicMap(map, Z80_MAIN_OPCODES);
    addRawOpcodeTableToMnemonicMap(map, Z80_CB_OPCODES);
    addRawOpcodeTableToMnemonicMap(map, Z80_ED_OPCODES);
    addRawOpcodeTableToMnemonicMap(map, Z80_DD_OPCODES);
    addRawOpcodeTableToMnemonicMap(map, Z80_FD_OPCODES);

    addArrayOpcodeTableToMnemonicMap(map, Z80_DDCB_OPCODES);
    addArrayOpcodeTableToMnemonicMap(map, Z80_FDCB_OPCODES);

    return map;
}

const std::unordered_map<Z80MnemonicKey, Z80InstructionInfo, Z80MnemonicKeyHash>
Z80_MNEMONIC_TO_OPCODE = buildMnemonicToOpcodeMap();
